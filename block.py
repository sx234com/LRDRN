# lightweight frequency-domain feature extraction module (LFFEM)
class LFFEM(nn.Module):
    def __init__(self, inp, oup, kernel_size=1, ratio=4, dw_size=3, stride=1, relu=True):
        super(LFFEM, self).__init__()
        self.oup = oup  #in 48 out 32 ratio
        selected_num=int(inp // ratio) #selected_num=12
        self.selected=selected_num

        remain_num=self.oup-selected_num

        if(selected_num>0):
            # å¼€å¯é€‰æ‹©çš„æƒ…å†µä¸‹
            self.CSS=CSSModule(kernel_size=3,a=ratio)

            # FDES
            self.ffc = FFC(in_channels=selected_num, out_channels=selected_num, kernel_size=kernel_size, ratio_gin=0, ratio_gout=0.5, stride=stride)

            # FRS
            self.feature_reuse = nn.Sequential(
                nn.Conv2d(selected_num, remain_num, dw_size, 1, dw_size // 2, groups=selected_num, bias=False),
                nn.BatchNorm2d(remain_num),
                nn.ReLU(inplace=True) if relu else nn.Sequential(),
            )

        else:
            #ä¸é€‰æ‹©çš„æƒ…å†µ
            selected_num = math.ceil(oup / ratio)
            remain_num = selected_num * (ratio - 1)

            # FDES
            self.ffc = FFC(in_channels=selected_num, out_channels=selected_num, kernel_size=kernel_size,
                                  ratio_gin=0, ratio_gout=0.5, stride=stride)


            # FRS
            self.feature_reuse = nn.Sequential(
                nn.Conv2d(selected_num, remain_num, dw_size, 1, dw_size // 2, groups=selected_num, bias=False),  ##ä½¿ç”¨ä¸€ç»´å·ç§¯ä½œä¸ºçº¿æ€§è®¡ç®—
                nn.BatchNorm2d(remain_num),
                nn.ReLU(inplace=True) if relu else nn.Sequential(),
            )


    def forward(self, x):
        if(self.selected>0):
            #CSS
            x,selected_num=self.CSS(x)
        #FDES
        x1 = self.ffc(x)
        #FRS
        x2 = self.feature_reuse(x1)
        out = torch.cat([x1,x2], dim=1)
        return out[:,:self.oup,:,:]


#Channel sparse submodule (CSS)
class CSSModule(nn.Module):

    def __init__(self, kernel_size=3, a=4):
        super().__init__()
        self.gap = nn.AdaptiveAvgPool2d(1)
        self.conv = nn.Conv1d(1, 1, kernel_size=kernel_size, padding=(kernel_size - 1) // 2)
        self.sigmoid = nn.Sigmoid()
        self.a = a  # è®¾ç½®é˜ˆå€¼ a ä»£è¡¨ä¿ç•™é€šé“æ•°é‡çš„ç™¾åˆ†æ¯”

    def init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                init.kaiming_normal_(m.weight, mode='fan_out')
                if m.bias is not None:
                    init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                init.constant_(m.weight, 1)
                init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                init.normal_(m.weight, std=0.001)
                if m.bias is not None:
                    init.constant_(m.bias, 0)

    def forward(self, x):
        # Global Average Pooling
        y = self.gap(x)  # bs,c,1,1
        y = y.squeeze(-1).permute(0, 2, 1)  # bs,1,c
        y = self.conv(y)  # bs,1,c
        y = self.sigmoid(y)  # bs,1,c
        y = y.permute(0, 2, 1).unsqueeze(-1)  # bs,c,1,1

        # è®¡ç®—é€šé“çš„æƒé‡
        channel_weights = y.squeeze(-1)  # bs,c,1 -> bs,c

        # æŒ‰ç…§é€šé“æƒé‡å¤§å°æ’åº
        sorted_weights, sorted_indices = torch.sort(channel_weights, dim=1, descending=True)

        # è·å–å‰ 30% æƒé‡å¤§äºaçš„é€šé“ç´¢å¼•
        num_channels = sorted_indices.size(1)
        num_select_channels = int(num_channels // self.a)  # 30%çš„é€šé“æ•°é‡

        # è·å–å‰30%çš„é€šé“ç´¢å¼•
        top_k_indices = sorted_indices[:, :num_select_channels]

        # è¿”å›ç­›é€‰å‡ºçš„é€šé“
        selected_channels = x.gather(1, top_k_indices.unsqueeze(-1).expand(-1, -1, x.size(2), x.size(3)))

        return selected_channels,num_select_channels




class FourierUnit(nn.Module):

    def __init__(self, in_channels, out_channels, groups=1):
        # bn_layer not used
        super(FourierUnit, self).__init__()
        self.groups = groups
        self.conv_layer = torch.nn.Conv2d(in_channels=in_channels * 2, out_channels=out_channels * 2,
                                          kernel_size=1, stride=1, padding=0, groups=self.groups, bias=False)
        self.bn = torch.nn.BatchNorm2d(out_channels * 2)
        self.relu = torch.nn.ReLU(inplace=True)

    def forward(self, x):
        batch, c, h, w = x.size()
        r_size = x.size()

        # (batch, c, h, w/2+1, 2)
        ffted = torch.rfft(x, signal_ndim=2, normalized=True)
        # (batch, c, 2, h, w/2+1)
        ffted = ffted.permute(0, 1, 4, 2, 3).contiguous()
        ffted = ffted.view((batch, -1,) + ffted.size()[3:])

        ffted = self.conv_layer(ffted)  # (batch, c*2, h, w/2+1)
        ffted = self.relu(self.bn(ffted))

        ffted = ffted.view((batch, -1, 2,) + ffted.size()[2:]).permute(
            0, 1, 3, 4, 2).contiguous()  # (batch,c, t, h, w/2+1, 2)

        output = torch.irfft(ffted, signal_ndim=2,
                             signal_sizes=r_size[2:], normalized=True)

        return output


class SpectralTransform(nn.Module):

    def __init__(self, in_channels, out_channels, stride=1, groups=1, enable_lfu=True):
        # bn_layer not used
        super(SpectralTransform, self).__init__()
        self.enable_lfu = enable_lfu
        if stride == 2:
            self.downsample = nn.AvgPool2d(kernel_size=(2, 2), stride=2)
        else:
            self.downsample = nn.Identity()

        self.stride = stride
        self.conv1 = nn.Sequential(
            nn.Conv2d(in_channels, out_channels //
                      2, kernel_size=1, groups=groups, bias=False),
            nn.BatchNorm2d(out_channels // 2),
            nn.ReLU(inplace=True)
        )
        self.fu = FourierUnit(
            out_channels // 2, out_channels // 2, groups)
        if self.enable_lfu:
            self.lfu = FourierUnit(
                out_channels // 2, out_channels // 2, groups)
        self.conv2 = torch.nn.Conv2d(
            out_channels // 2, out_channels, kernel_size=1, groups=groups, bias=False)

    def forward(self, x):

        x = self.downsample(x)
        x = self.conv1(x)
        output = self.fu(x)

        if self.enable_lfu:
            n, c, h, w = x.shape
            split_no = 2
            split_s_h = h // split_no
            split_s_w = w // split_no
            xs = torch.cat(torch.split(
                x[:, :c // 4], split_s_h, dim=-2), dim=1).contiguous()
            xs = torch.cat(torch.split(xs, split_s_w, dim=-1),
                           dim=1).contiguous()
            xs = self.lfu(xs)
            xs = xs.repeat(1, 1, split_no, split_no).contiguous()
        else:
            xs = 0

        output = self.conv2(x + output + xs)

        return output

# Fast Fourier Convolution
class FFC(nn.Module):

    def __init__(self, in_channels, out_channels, kernel_size,
                 ratio_gin, ratio_gout, stride=1, padding=0,
                 dilation=1, groups=1, bias=False, enable_lfu=True):
        super(FFC, self).__init__()

        assert stride == 1 or stride == 2, "Stride should be 1 or 2."
        self.stride = stride

        in_cg = int(in_channels * ratio_gin)
        in_cl = in_channels - in_cg
        out_cg = int(out_channels * ratio_gout)
        out_cl = out_channels - out_cg
        #groups_g = 1 if groups == 1 else int(groups * ratio_gout)
        #groups_l = 1 if groups == 1 else groups - groups_g

        self.ratio_gin = ratio_gin
        self.ratio_gout = ratio_gout

        module = nn.Identity if in_cl == 0 or out_cl == 0 else nn.Conv2d
        self.convl2l = module(in_cl, out_cl, kernel_size,
                              stride, padding, dilation, groups, bias)
        module = nn.Identity if in_cl == 0 or out_cg == 0 else nn.Conv2d
        self.convl2g = module(in_cl, out_cg, kernel_size,
                              stride, padding, dilation, groups, bias)
        module = nn.Identity if in_cg == 0 or out_cl == 0 else nn.Conv2d
        self.convg2l = module(in_cg, out_cl, kernel_size,
                              stride, padding, dilation, groups, bias)
        module = nn.Identity if in_cg == 0 or out_cg == 0 else SpectralTransform
        self.convg2g = module(
            in_cg, out_cg, stride, 1 if groups == 1 else groups // 2, enable_lfu)

    def forward(self, x):
        x_l, x_g = x if type(x) is tuple else (x, 0)
        out_xl, out_xg = 0, 0

        if self.ratio_gout != 1:
            out_xl = self.convl2l(x_l) + self.convg2l(x_g)
        if self.ratio_gout != 0:
            out_xg = self.convl2g(x_l) + self.convg2g(x_g)

        out=torch.cat((out_xl, out_xg),dim=1) #æ‹¼æ¥èåˆ

        return out


